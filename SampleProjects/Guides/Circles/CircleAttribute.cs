namespace Circles;

/// <summary>
/// Represents different attributes of a circle.
/// </summary>
public enum CircleAttribute
{
    /// <summary>
    /// The width of the rectangle.
    /// </summary>
    Radius,

    /// <summary>
    /// The color of the rectangle.
    /// </summary>
    Color,

    /// <summary>
    /// The border thickness of the rectangle.
    /// </summary>
    BorderThickness,

    /// <summary>
    /// Solid state of the rectangle.
    /// </summary>
    IsSolid,

    /// <summary>
    /// The gradient type of the rectangle.
    /// </summary>
    GradientType,

    /// <summary>
    /// The start color of the gradient.
    /// </summary>
    GradientStart,

    /// <summary>
    /// The stop color of the gradient.
    /// </summary>
    GradientStop,
}
